// 计算每位球星的理论最高分（仅普通题，不含+5主线）
function calcMaxScores() {
  const max = new Array(30).fill(0)
  Object.keys(weights).forEach(key => {
    weights[key].forEach(([idx, val]) => {
      if (val < 5) max[idx] += val
    })
  })
  return max
}

// 主计算函数，传入答案对象 { Q1: 'A', Q2: 'C', ... }
// 返回前三相似球星数组 [{name, idx, pct, rawScore}, ...]
function calcResult(answers) {
  const maxScores = calcMaxScores()
  const rawScores = new Array(30).fill(0)
  const regretBonus = new Array(30).fill(0)

  Object.keys(answers).forEach(qid => {
    const key = qid + answers[qid]
    if (weights[key]) {
      weights[key].forEach(([idx, val]) => {
        rawScores[idx] += val
        if (val === 5) regretBonus[idx] += val
      })
    }
  })

  const ranked = starNames.map((name, i) => {
    const normalScore = rawScores[i] - regretBonus[i]
    const maxNormal = maxScores[i]
    const basePct = maxNormal > 0 ? normalScore / maxNormal : 0
    const regretBoost = regretBonus[i] > 0 ? Math.min(regretBonus[i] * 0.05, 0.25) : 0
    const finalPct = Math.min(Math.round((basePct + regretBoost) * 100), 99)
    return { name, idx: i, pct: finalPct, rawScore: rawScores[i] }
  }).sort((a, b) => b.pct - a.pct || b.rawScore - a.rawScore)

  return ranked.slice(0, 3)
}

